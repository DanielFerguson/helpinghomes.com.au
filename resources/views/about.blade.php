            <h2>Offers</h2>

            <p>
                Helping Homes cannot visit places that are offered, nor can we interview anyone who wants to offer facilities, or take up an offer. We are simply a noticeboard helping to link people together - people are free to make their own arrangements with each other by talking directly with each other, using the contact information provided. The locations listed on each of the offer pins are not the offerer's address, rather a close representation.
            </p>
            <p>
                It may be useful to keep a couple of things in mind:
            <ul>
                <li>People are people: we all come in a range of personality styles and have different values, and ways of living. “Respect” is a key ingredient in times like these, especially as many of us may be experiencing emotional challenges due to the events. </li>
                <li>
                    Early conversations between those sharing and those needing assistance may be more useful if they include topics like:
                    <ul>
                        <li>What is being offered / what is not being offered?</li>
                        <li>What (if any) financial expectations there are (food / utilities etc)?</li>
                        <li> Time periods for which the offer may be valid?</li>
                        <li>How you might resolve issues if / when they occur?</li>
                    </ul>
                </li>
            </ul>
            </p>
            <p>
                The spirit of Helping Homes is about “people helping people”; something that we seem to be good at in Australia during a crisis. There will always be a small fraction of people who use such times as an opportunity for personal gain, but we hope that with good communication and respect, everyone's experience can be positive.
            </p>
